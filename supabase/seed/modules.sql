**Neuromarketing**

Neuromarketing menerapkan ilmu saraf dan psikologi untuk memahami bagaimana konsumen benar-benar membuat keputusan.

Insight kunci: otak pengambil keputusan adalah emotional brain (limbic system), bukan logical brain (neocortex). Rasionalisasi datang setelah keputusan dibuat.

**Cognitive Biases yang Relevan:**

**1. Anchoring Effect**
Harga pertama yang dilihat menjadi "jangkar".
→ Tampilkan harga coret (harga asli → harga diskon)

**2. Loss Aversion**
Kehilangan terasa 2x lebih kuat dari mendapatkan sesuatu.
→ "Jangan lewatkan" > "Dapatkan sekarang"

**3. Social Proof**
Kita cenderung melakukan apa yang orang lain lakukan.
→ Testimonial, counter ("10.000+ pengguna"), star rating

**4. Scarcity & Urgency**
Sesuatu yang langka terasa lebih bernilai.
→ Limited stock, countdown timer, early bird

**5. Decoy Effect**
Opsi ketiga yang tidak menarik membuat satu opsi lain terlihat lebih baik.
→ Pricing tiers: Basic / Pro (highlight) / Enterprise

**6. The Paradox of Choice**
Terlalu banyak pilihan membuat orang tidak memilih.
→ Batasi opsi, beri rekomendasi

**7. Authority Bias**
Kita mempercayai figur otoritatif.
→ Ekspert, sertifikasi, media features, "Seperti kata..."

